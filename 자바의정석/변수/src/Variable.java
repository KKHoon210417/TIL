public class Variable {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);

        x = 10;
        System.out.println(x);

        System.out.println("----------------");

        x = 16;
        int y = 8;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
    }
}
